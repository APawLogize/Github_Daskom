<h2>Pengenalan Apa Itu Framework</h2>
<p>Pertama, kita harus mengenal terlebih dahulu apa itu framework dan fungsi – fungsi apa saja yang dimilikinya. Nah, dari situ anda dapat mulai memahami tujuan dari penggunaannya dalam pengembangan website ataupun perangkat lunak.</p>
<h3>1. Pengertian Framework</h3>
<p>Sesuai dengan namanya sendiri, framework adalah kerangka kerja untuk mengembangkan aplikasi berbasis website maupun desktop. Kerangka kerja disini sangat membantu developer dalam menuliskan sebuah dengan lebih terstruktur dan tersusun rapi.</p>
<p>Kerangka kerja diciptakan untuk mempermudah kinerja dari programmer. Sehingga, seorang programmer tidak perlu untuk menuliskan kode secara berulang – ulang. Karena di dalamnya sendiri anda hanya perlu menyusun komponen – komponen pemrograman saja.</p>
<h3>2. Fungsi Framework</h3>
<p>Sebagai developer, tentu harus mengetahui tujuan dari penggunaannya untuk kepentingan pembuatan aplikasi. Sehingga, untuk proses pengerjaan aplikasi dapat dilakukan dengan menggunakan framework yang tepat dan sesuai dengan kebutuhan project. Berikut merupakan beberapa fungsi kerangka kerja dalam web development.</p>
<h4> a. Kode program lebih terstruktur</h4>
<h4>b. Membantu kinerja dari developer</h4>
<h4>c. Meningkatkan keamanan perangkat lunak / website</h4>
<h4>d. Pemeliharaan dan dokumentasi dari website lebih mudah</h4>
<h4>e. Mempercepat dalam proses pembuatan website</h4>

